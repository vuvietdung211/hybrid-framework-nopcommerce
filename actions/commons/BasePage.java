	public void switchtoWindowByID(WebDriver driver, String windowPageID) {
		Set<String> allWindows = driver.getWindowHandles();
		for (String window : allWindows) {
			if (window.equals(windowPageID)) {
				driver.switchTo().window(window);
				break;
			}
		}
	}

	public void switchtoWindowByPageTitleID(WebDriver driver, String expectedpageTitle) {
		Set<String> allWindows = driver.getWindowHandles();
		for (String window : allWindows) {
			driver.switchTo().window(window);
			sleepInSecond(1);
			String actualPageTitle = driver.getTitle().trim();
			if (actualPageTitle.equals(expectedpageTitle)) {
				break;
			}
		}
	}

	public void closeAllWindowByPageWithoutParent(WebDriver driver, String parentpageID) {
		Set<String> allWindows = driver.getWindowHandles();
		for (String window : allWindows) {
			if (!window.equals(parentpageID)) {
				driver.switchTo().window(window);
				driver.close();
			}
		}
		driver.switchTo().window(parentpageID);
		sleepInSecond(2);

	}

	public void sleepInSecond(long second) {
		try {
			Thread.sleep(second * 1000);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
	}

	private WebElement getWebElement(WebDriver driver, String xpathLocator) {
		return driver.findElement(getByXpath(xpathLocator));
	}
	
	private List<WebElement> getListWebElements(WebDriver driver, String xpathLocator) {
		return driver.findElements(getByXpath(xpathLocator));
	}
	
	private By getByXpath(String xpathLocator) {
		return By.xpath(xpathLocator);
	}

	//locatorType: id=/ css=/ xpath=/ name=/ class=
	private By getByLocator(String locatorType) {
		By by = null;
		if (locatorType.startsWith("id=")) {
			by = By.id(locatorType.substring(3));
		} else if (locatorType.startsWith("class=")) {
			by = By.id(locatorType.substring(6));
		} else  if (locatorType.startsWith("name=")) {
			by = By.id(locatorType.substring(5));
		} else if (locatorType.startsWith("xpath=")) {
			by = By.id(locatorType.substring(6));
		} else if (locatorType.startsWith("css=")) {
			by = By.id(locatorType.substring(4));
		} else {
			throw new RuntimeException("Locator type is not supported");
		}
		return by;

	}
		
