package EclipseTips;

public class Topic_03_Getter_Setter {
	private String carName;
	private String carType;
	private String carColor;
	private String carMachine;
	/**
	 * get car name
	 * @author Admin
	 * @return the carName
	 */
	public final String getCarName() {
		return carName;
	}
	/**
	 * @param carName the carName to set
	 */
	public final void setCarName(String carName) {
		this.carName = carName;
	}
	/**
	 * @return the carType
	 */
	public final String getCarnType() {
		return carType;
	}
	/**
	 * @param carType the carnType to set
	 */
	public final void setCarnType(String carnType) {
		this.carType = carnType;
	}
	/**
	 * @return the carColor
	 */
	public final String getCarColor() {
		return carColor;
	}
	/**
	 * @param carColor the carColor to set
	 */
	public final void setCarColor(String carColor) {
		this.carColor = carColor;
	}
	/**
	 * @return the carMachine
	 */
	public final String getCarMachine() {
		return carMachine;
	}
	/**
	 * @param carMachine the carMachine to set
	 */
	public final void setCarMachine(String carMachine) {
		this.carMachine = carMachine;
	}
	
	
}
